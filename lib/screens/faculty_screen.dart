class Department {
  final String name;
  final String code;
  final String head;
  final String description;
  final String website;
  final String email;
  final String phone;
  final String address;
  final List<Program> programs;

  Department({
    required this.name,
    required this.code,
    required this.head,
    required this.description,
    required this.website,
    required this.email,
    required this.phone,
    required this.address,
    required this.programs,
  });
}

class Program {
  final String code;
  final String name;
  final String degree;
  final String duration;
  final String form;
  final List<ProgramProfile> profiles;

  Program({
    required this.code,
    required this.name,
    required this.degree,
    required this.duration,
    required this.form,
    required this.profiles,
  });
}

class ProgramProfile {
  final String name;
  final String description;

  ProgramProfile({
    required this.name,
    required this.description,
  });
}